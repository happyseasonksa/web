     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Page  $page
     * @return \Illuminate\Http\Response
     */
    public function update(Request $req, $id)
    {
        $this->validate($req, [
            // 'name'   => 'required|string|max:255',
            'title'   => 'required|string|max:255',
            'content'   => 'required|string',
        ]);
        $data = $req->all();
        $page = Page::find($id);
        if ($page) {
            $page = $page->updatePage($data,$page);
            return redirect()->route('admin.page.index')->with('toast-success', __('Successfully added').'!');
        }
        return redirect()->back()->with('toast-error', 'Page not found!');
    }

